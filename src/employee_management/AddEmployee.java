    private void tfTeamInfoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfTeamInfoKeyReleased
        String PATTERN="^[a-zA-Z0-9.-]+$";
        Pattern patt = Pattern.compile(PATTERN);
        Matcher match= patt.matcher(tfTeamInfo.getText());
        String id=tfTeamInfo.getText();
        int length = id.length();

        if(!match.matches()){
            teamInfoLabel.setText("Incorrect");
        }
        else{
            if (length <21) {
                teamInfoLabel.setText(null);
                tfTeamInfo.setEditable(true);

            } else {
                tfTeamInfo.setEditable(false);
                teamInfoLabel.setText("Not so long");
            }
        }
    }//GEN-LAST:event_tfTeamInfoKeyReleased

