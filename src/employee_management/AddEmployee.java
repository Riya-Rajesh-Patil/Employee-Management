    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        String name = tfName.getText();
        String age = tfAge.getText();
        String gender = tfGender.getText();
        String email = tfEmail.getText();
        String phone = tfPhone.getText();
        String empID = tfEmpId.getText();
        String startDate = tfStartDate.getText();
        String level = tfLevel.getText();
        String teamInfo = tfTeamInfo.getText();
        String position = tfPosition.getText();
        String image = imagePath;
        String selectedImagePath = "";

        if(name.isEmpty() || age.isEmpty()||gender.isEmpty()||email.isEmpty()|| phone.isEmpty()||empID.isEmpty()||startDate.isEmpty()||level.isEmpty()||teamInfo.isEmpty() || position.isEmpty()||image==null){
            JOptionPane.showMessageDialog(this,
                "Please enter all the fields",
                "Try Again",
                JOptionPane.ERROR_MESSAGE);
        }
        else{

            DefaultTableModel model = (DefaultTableModel) tableEmployees.getModel();

            model.addRow(new Object[] {name,age,gender, email, phone, empID, startDate, level, teamInfo, position, imagePath});

            JOptionPane.showMessageDialog(null, "Data Inserted");
            clearFields();
        }
    }//GEN-LAST:event_btnAddActionPerformed

